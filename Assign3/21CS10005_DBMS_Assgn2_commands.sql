-- Create tables according to ER Diagram
CREATE TABLE ROLE (RID INT PRIMARY KEY, RName VARCHAR(255), Description VARCHAR(2000));
CREATE TABLE STUDENT (Name VARCHAR(255), Roll CHAR(9) PRIMARY KEY, Dept VARCHAR(10), RID INT REFERENCES ROLE(RID));
CREATE TABLE EVENT (EID INT PRIMARY KEY, EName VARCHAR(255), Date DATE, Type VARCHAR(255));
CREATE TABLE VOLUNTEER (Roll CHAR(9) PRIMARY KEY REFERENCES STUDENT(Roll));
CREATE TABLE PARTICIPANT (PID INT PRIMARY KEY, Name VARCHAR(255), College_Name VARCHAR(255), College_Location VARCHAR(255));
CREATE TABLE MANAGE (Roll CHAR(9) REFERENCES STUDENT(Roll), EID INT REFERENCES EVENT(EID));
CREATE TABLE EVENT_PART (EID INT REFERENCES EVENT(EID), PID INT REFERENCES PARTICIPANT(PID));
CREATE TABLE EVENT_VOL (EID INT REFERENCES EVENT(EID), Vol_Roll CHAR(9) REFERENCES VOLUNTEER(Roll));

